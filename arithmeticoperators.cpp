#include<stdio.h>

int main()
{
	int a=50,b=34;
	int c,d,e,f,g,h,i;
	/*Arithmetic operators */
	c=a+b ;
	printf("the addition of a and b is %d \n",c);
	d=a-b ;
	printf("the subraction of a and b is %d \n",d);
	e=a*b ;
	printf("the multiplication of a and b is %d \n",e);
	f=a/b ;
	printf("the division of a and b is %d \n",f);
	g=a%b;
	printf("the value of g is %d \n",g);
	h=a++;
	printf("the value of h is %d \n",h);
	i=b--;
	printf("the value of i is %d \n",i);
	
	return 0;
	
}
