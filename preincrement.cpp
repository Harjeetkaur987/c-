#include<stdio.h>

int main()
{
	int a,b,c;
	printf("enter the value of a \n");
	scanf("%d",&a);
	printf("enter the value of b\n");
	scanf("%d",&b);
	printf("enter the value of c\n");
	scanf("%d",&c);
	 
	++a;
	--b;
	++c;
	
	printf("the updated value of a=%d\n",a);
	printf("the updated value of b=%d\n",b);
	printf("the updated value of c=%d\n",c);
	return 0;
	
}
	
	
	
